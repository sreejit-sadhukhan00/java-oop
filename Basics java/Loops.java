public class Loops {
    public static void main(String[] args) {
        int a=5;
    // While Loop
        while (a<=10) {
            // System.out.println(a);
            a++;
        }

    // for Loop
       for(int b=10; b>=0;b--){
        // System.out.println(b);
       }

    //    Do While Loop
    int c=5;
        do{
                  System.out.println(c);
                  c++;
        }
        while(c<10);
    }
   
}
